'''
Two numbers are entered by user. Write a program to
swap content or value of two numbers.
'''
print("Enter value of X:")
x=input() #x=10
a=int(x)  #a=10

print("Enter value of Y:")
y=input() #y=20
b=int(y)  #b=20

print("Value before swapping")
print("X:",a) #X:10
print("Y:",b) #Y:20

#Swapping logic
t=a #t=10
a=b #a=20
b=t #b=10

print("Value after swapping")
print("X:",a)
print("Y:",b)


